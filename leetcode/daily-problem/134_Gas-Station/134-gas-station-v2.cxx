template<typename T>
inline void coutVector( std::vector<T> vec, std::string label = "Vector" ){
  std::cout << label << " : { ";
  for( size_t i = 0 ; i < (vec.size()-1); i++ ){
    std::cout << vec.at(i) << "; " ;
  }
  std::cout<< vec.back() << " }"<< std::endl;
}

class Solution {
public:

//----------------------------------------------------------- Challenge Solution
  class Circuit
  {
  public:
    int pGas;
    Circuit* pNext;
    Circuit( int& g , int& c ){
      pGas = g - c ;

    }
    ~Circuit(){};
    void setNext( Circuit* next ){
      pNext = next;
    }

    bool canCompleteCircuit( int length ){
      Circuit* next = this;
      int gas{0};
      while ( length )
      {
        gas += next->pGas;
        if( gas < 0 ){ return false; }
        next = next->pNext;
        length--;
        std::cout << "->"<< gas ;
      }
      return true;
    }

    static Circuit* build( vector<int>& gas, vector<int>& cost ){
      Circuit* first = new Circuit( gas.front(), cost.front() );
      Circuit* prev = first;
      for (size_t i = 1; i < gas.size() ; i++)
      {
        prev->setNext( new Circuit(gas.at(i), cost.at(i) ) );
        prev = prev->pNext;
      }
      prev->setNext(first);
      return first;
    }
  };

  int canCompleteCircuit(vector<int>& gas, vector<int>& cost) {
    // if only one station return index 0
    size_t circuitLength = gas.size();
    if( circuitLength == 0 ) return 0;

    Circuit* circuit = Circuit::build( gas, cost );
    for (size_t i = 0; i < circuitLength ; i++)
    {
      std::cout << " S("<< circuit->pGas <<") -> START "<< std::endl;
      if( circuit->canCompleteCircuit( circuitLength ) ) return i;
      circuit = circuit->pNext;
    }
    return -1;
  }
//------------------------------------------------------------------------------
  void showCanCompleteCircuit(vector<int> gas, vector<int> cost) {
    std::cout << "----- NEW TEST -----" << std::endl;
    coutVector(gas ,"GAS");
    coutVector(cost, "COST");
    int result{ canCompleteCircuit(gas, cost ) };
    if( -1 == result ){
      std::cout << "Can Not Complete Circuit" << std::endl;
    } else {
      std::cout << "Starting Index: "<< result << std::endl;
    }
  }
};

